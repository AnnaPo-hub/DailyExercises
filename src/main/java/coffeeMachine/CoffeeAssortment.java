package coffeeMachine;

public class CoffeeAssortment {
    int amountOfWater;
    int amountOfMilk;
    int amountOfBeans;
    int amountOfCups;
    int price;

    public CoffeeAssortment(int amountOfWater, int amountOfMilk, int amountOfBeans, int amountOfCups, int price) {
        this.amountOfWater = amountOfWater;
        this.amountOfMilk = amountOfMilk;
        this.amountOfBeans = amountOfBeans;
        this.amountOfCups = amountOfCups;
        this.price = price;
    }
}
